import json
from typing import List, Dict, Any, Tuple
import math

class TrajectoryScorer:
    """
    Evaluates the continuous-time JSON traces generated by the Orchestrator
    to mathematically prove the presence (or absence) of active inference.
    """
    
    def __init__(self, trace_log: List[Dict[str, Any]], fault_injection_turn: int = 3):
        self.log = trace_log
        self.t_fault = fault_injection_turn

    def _find_quarantine_turn(self, suspected_node: str = "Node_C") -> int:
        """Finds the turn where the orchestrator definitively dropped trust in the node to near zero."""
        for turn, state in enumerate(self.log):
            if state.get("confidence_scores", {}).get(suspected_node, 1.0) < 0.2:
                return turn
        return -1

    def calculate_fault_isolation_latency(self) -> float:
        """
        Delta-t: How fast the Orchestrator updates its belief after the contradiction.
        If it never quarantines the node, returns positive infinity.
        """
        quarantine_turn = self._find_quarantine_turn()
        if quarantine_turn == -1:
            return float('inf')
        
        latency = quarantine_turn - self.t_fault
        # Latency can be negative if it preemptively distrusts, but we clip to 0 for scoring
        return max(0.0, float(latency))

    def calculate_perseveration_index(self, faulty_node: str = "Node_C") -> int:
        """
        P-score: Counts how many times the orchestrator attempts to query
        the faulty node AFTER the contradiction has been mathematically proven by a previous query.
        """
        p_index = 0
        contradiction_observed = False
        
        for turn, state in enumerate(self.log):
            action = state.get("next_action", {})
            
            if turn >= self.t_fault and action.get("type") == "query" and action.get("target_agent") == faulty_node:
                if contradiction_observed:
                    p_index += 1
                else:
                    # After first query post-fault, contradiction is visible
                    contradiction_observed = True
                    
        return p_index
        
    def calculate_epistemic_entropy(self) -> List[float]:
        """
        Calculates Shannon entropy H over the reported confidence distributions.
        High entropy -> Confusion/Uncertainty. 
        Low entropy -> Confidence in the world model.
        """
        entropies = []
        for state in self.log:
            scores = state.get("confidence_scores", {})
            total_prob = sum(scores.values()) if sum(scores.values()) > 0 else 1.0
            
            # Normalize to valid probability distribution
            norm_scores = [v/total_prob for v in scores.values()]
            
            h = -sum(p * math.log(p + 1e-9) for p in norm_scores if p > 0)
            entropies.append(h)
        return entropies
        
    def to_report(self) -> Dict[str, Any]:
        """Generates a comprehensive diagnostic profile."""
        latency = self.calculate_fault_isolation_latency()
        perseveration = self.calculate_perseveration_index()
        entropy_traj = self.calculate_epistemic_entropy()
        
        # A true AGI should have low latency (<= 2) and exactly 0 perseveration.
        # Current autoregressive models will likely score inf latency and P > 2.
        
        return {
            "cognitive_profile": {
                "fault_isolation_latency": latency,
                "perseveration_index": perseveration,
                "entropy_trajectory_H": entropy_traj,
                "agi_test_passed": latency != float('inf') and perseveration == 0
            }
        }
